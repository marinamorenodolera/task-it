import { useState, useEffect, createContext, useContext } from 'react'
import { supabase } from '@/lib/supabase'

const AuthContext = createContext({})

export const useAuth = () => {
  const context = useContext(AuthContext)
  if (!context) {
    throw new Error('useAuth must be used within AuthProvider')
  }
  return context
}

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null)
  const [loading, setLoading] = useState(true)
  const [profile, setProfile] = useState(null)

  console.log('üîê AuthProvider - RENDER')
  console.log('üîê Estado actual:', { user: !!user, loading, profile: !!profile })
  console.log('üîê *** LOADING STATE:', loading, '***')
  
  // üö® DIAGN√ìSTICO ESPEC√çFICO PARA USUARIO AUTENTICADO
  if (user && loading) {
    console.log('üö® PROBLEMA DETECTADO: Usuario existe pero loading=true')
    console.log('üö® Usuario:', user.email || user.id)
    console.log('üö® Este es el problema que causa el spinner infinito')
    
    // AGGRESSIVE FIX: Try to force loading to false immediately when we detect this issue
    console.log('üö® INTENTANDO FORCE RESET INMEDIATO')
    setTimeout(() => {
      console.log('üö® EJECUTANDO setLoading(false) INMEDIATO')
      setLoading(false)
    }, 100) // Very short delay to break any potential sync issues
  }

  useEffect(() => {
    console.log('üîê AuthProvider - useEffect ejecutado')
    console.log('üöÄ INITIALIZATION LOGGING')
    console.log('Environment check:')
    console.log('- NODE_ENV:', process.env.NODE_ENV)
    console.log('- Supabase URL configured:', !!process.env.NEXT_PUBLIC_SUPABASE_URL)
    console.log('- Supabase Key configured:', !!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY)
    
    // Get initial session with better session persistence handling
    const getInitialSession = async () => {
      console.log('üîê Verificando sesi√≥n inicial...')
      console.log('üîê Estado inicial loading antes de verificar:', loading)
      
      try {
        // First, wait a bit to let Supabase potentially hydrate from localStorage
        // This helps with session persistence on page reload
        console.log('üîê Esperando hidrataci√≥n de Supabase...')
        await new Promise(resolve => setTimeout(resolve, 100))
        
        console.log('üîê Llamando a supabase.auth.getSession()...')
        const { data: { session }, error } = await supabase.auth.getSession()
        console.log('üîê Resultado getSession:', { 
          session: !!session, 
          user: !!session?.user, 
          error: !!error,
          sessionId: session?.access_token?.substring(0, 20) + '...',
          userEmail: session?.user?.email
        })
        
        if (error) {
          console.error('üîê Error en getSession:', error)
          console.log('üîê Estableciendo user a null debido a error')
          setUser(null)
        } else if (session?.user) {
          console.log('üîê ‚úÖ Sesi√≥n PERSISTENTE encontrada, configurando usuario:', session.user.email)
          setUser(session.user)
          
          // Cargar perfil sin bloquear el finally
          try {
            console.log('üîê Cargando perfil de usuario...')
            await loadUserProfile(session.user.id)
            console.log('üîê Perfil cargado exitosamente')
          } catch (profileError) {
            console.error('üîê Error cargando perfil, pero continuando:', profileError)
            // No bloquear la app si falla el perfil
          }
          
          console.log('üîê Usuario configurado, continuando al finally para resetear loading')
        } else {
          console.log('üîê ‚ùå No hay sesi√≥n persistente - usuario debe hacer login')
          setUser(null)
        }
      } catch (error) {
        console.error('üîê Error capturado en getInitialSession:', error)
        console.error('üîê Stack trace:', error.stack)
        setUser(null)
      } finally {
        console.log('üîê EJECUTANDO FINALLY - Finalizando verificaci√≥n inicial')
        console.log('üîê Estado loading antes de resetear:', loading)
        console.log('üîê *** CR√çTICO: setLoading(false) ***')
        setLoading(false)
        console.log('üîê setLoading(false) ejecutado exitosamente')
      }
    }

    getInitialSession()

    // Safety fallback: If loading is still true after 10 seconds, force it to false
    // This prevents infinite loading if something goes wrong with Supabase
    const safetyTimer = setTimeout(() => {
      console.log('üö® SAFETY FALLBACK: Checking if loading needs to be forced to false after 10 seconds')
      // Use setLoading with a callback to access current state
      setLoading(currentLoading => {
        if (currentLoading) {
          console.log('üö® Ejecutando setLoading(false) por timeout de seguridad')
          return false
        }
        console.log('üö® Loading ya est√° en false, no es necesario cambiar')
        return currentLoading
      })
    }, 10000) // 10 seconds

    // Faster safety check for authenticated users - simplified approach
    const authenticatedUserTimer = setTimeout(() => {
      console.log('üö® SAFETY CHECK: Verificando estado despu√©s de 3 segundos')
      console.log('üö® Estado actual en timer: loading=', loading, 'user=', !!user)
      
      // Simple force to false after 3 seconds regardless of user state
      // This should break any infinite loading loop
      console.log('üö® FORZANDO setLoading(false) despu√©s de 3 segundos')
      setLoading(false)
    }, 3000) // 3 seconds aggressive timeout

    // Listen for auth changes with extensive logging
    console.log('üîê Configurando listener de auth state changes...')
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîê Auth state change detectado:', {
          event,
          session: !!session,
          user: !!session?.user,
          timestamp: new Date().toISOString()
        })
        console.log('üîê Estado loading al inicio del auth change:', loading)
        
        try {
          setUser(session?.user ?? null)
          
          if (session?.user) {
            console.log('üîê Nuevo usuario logueado:', session.user.email)
            
            // Cargar perfil sin bloquear el finally
            try {
              await loadUserProfile(session.user.id)
              console.log('üîê Perfil cargado en auth change')
            } catch (profileError) {
              console.error('üîê Error cargando perfil en auth change:', profileError)
            }
          } else {
            console.log('üîê Usuario deslogueado, limpiando profile')
            setProfile(null)
          }
        } catch (error) {
          console.error('üîê Error en onAuthStateChange:', error)
        } finally {
          console.log('üîê *** CR√çTICO: Auth state change completado, setLoading(false) ***')
          setLoading(false)
          console.log('üîê setLoading(false) ejecutado en onAuthStateChange')
        }
      }
    )

    return () => {
      console.log('üîê Limpiando subscription de auth changes')
      console.log('üîê Limpiando safety timers')
      clearTimeout(safetyTimer)
      clearTimeout(authenticatedUserTimer)
      subscription.unsubscribe()
    }
  }, [])

  const loadUserProfile = async (userId) => {
    console.log('üë§ loadUserProfile - INICIO para userId:', userId)
    console.log('üë§ Estado loading antes de cargar perfil:', loading)
    
    try {
      const { data, error } = await supabase
        .from('user_profiles')
        .select('*')
        .eq('id', userId)
        .single()

      if (error && error.code !== 'PGRST116') {
        console.error('üë§ Error loading profile:', error)
        console.log('üë§ Error code:', error.code, 'continuando sin perfil...')
        setProfile(null)
        return null
      }

      console.log('üë§ Perfil cargado exitosamente:', !!data)
      setProfile(data)
      return data
    } catch (error) {
      console.error('üë§ Error capturado en loadUserProfile:', error)
      setProfile(null)
      return null
    }
  }

  const signUp = async (email, password, username) => {
    try {
      setLoading(true)
      
      // üîç SUPABASE VERIFICATION
      console.log('üîç Supabase verification:')
      console.log('- URL:', process.env.NEXT_PUBLIC_SUPABASE_URL)
      console.log('- Anon Key exists:', !!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY)
      console.log('- Key prefix:', process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY?.substring(0, 20) + '...')
      console.log('- Project host:', new URL(process.env.NEXT_PUBLIC_SUPABASE_URL).hostname)
      
      // üè• CONNECTION TEST
      console.log('üè• Testing Supabase connection...')
      const connectionTest = await supabase.auth.getSession()
      console.log('Connection test result:', connectionTest.error ? 'FAILED' : 'SUCCESS')
      if (connectionTest.error) {
        console.error('Connection error:', connectionTest.error)
      }
      
      // üìù TEST 1: SIGNUP CON USERNAME
      console.log('üìù Test 1: Attempting signup with username metadata...')
      console.log('Email:', email)
      console.log('Username for metadata:', username)
      
      const signUpData = {
        email,
        password,
        options: {
          data: {
            username: username,
            display_name: username
          }
        }
      }
      
      console.log('üì§ Signup request payload:', signUpData)
      
      const { data, error } = await supabase.auth.signUp(signUpData)
      
      // üìß DETAILED RESPONSE LOGGING
      console.log('üìß Detailed signup response:')
      console.log('- Data exists:', !!data)
      console.log('- User created:', !!data?.user)
      console.log('- User ID:', data?.user?.id)
      console.log('- User email:', data?.user?.email)
      console.log('- User metadata:', data?.user?.user_metadata)
      console.log('- Session exists:', !!data?.session)
      console.log('- Error exists:', !!error)
      
      if (error) {
        // ‚ùå DETAILED ERROR ANALYSIS
        console.error('‚ùå DETAILED SUPABASE ERROR:')
        console.error('- Error message:', error.message)
        console.error('- Error code:', error.status)
        console.error('- Error details:', error.details)
        console.error('- Error hint:', error.hint)
        console.error('- Full error object:', error)
        
        // üîÑ TEST 2: FALLBACK SIN USERNAME
        console.log('üîÑ Test 2: Attempting signup without username metadata...')
        
        const fallbackData = {
          email: email.includes('.test') ? email : email + '.test', // Avoid email conflicts
          password
        }
        
        const { data: data2, error: error2 } = await supabase.auth.signUp(fallbackData)
        
        if (error2) {
          console.error('‚ùå Fallback signup also failed:', error2)
        } else {
          console.log('‚úÖ Fallback signup succeeded!')
          console.log('User created without metadata:', data2?.user?.id)
        }
        
        // üí° AUTOMATIC DIAGNOSIS
        console.log('üí° DIAGNOSIS:')
        if (error.message?.includes('permission denied')) {
          console.log('- LIKELY CAUSE: RLS (Row Level Security) blocking user creation')
          console.log('- SOLUTION: Check RLS policies on auth.users or related tables')
        } else if (error.message?.includes('duplicate')) {
          console.log('- LIKELY CAUSE: User already exists with this email')
          console.log('- SOLUTION: Use different email or implement login flow')
        } else if (error.message?.includes('invalid')) {
          console.log('- LIKELY CAUSE: Invalid email format or weak password')
          console.log('- SOLUTION: Validate inputs before sending to Supabase')
        } else if (error.message?.includes('metadata')) {
          console.log('- LIKELY CAUSE: Issue with user_metadata field')
          console.log('- SOLUTION: Remove username from metadata or fix trigger')
        } else if (error.message?.includes('Database error saving new user')) {
          console.log('- CONFIRMED CAUSE: Database trigger or RLS policy failure')
          console.log('- SOLUTION: This is a Supabase configuration issue, not a code issue')
          console.log('- ACTION: Check Supabase Dashboard > Authentication > Settings')
          console.log('- ACTION: Verify no blocking triggers or RLS policies on auth.users')
        } else {
          console.log('- UNKNOWN ERROR: Check Supabase dashboard logs for more details')
        }
        
        throw error
      }
      
      // ‚úÖ SUCCESS CASE
      console.log('‚úÖ Signup successful!')
      console.log('User ID:', data.user.id)
      
      if (data.user && data.session) {
        console.log('‚úÖ User logged in automatically')
      } else {
        console.log('‚ö†Ô∏è User created but not logged in (email confirmation required?)')
      }

      return { data, error: null }
      
    } catch (error) {
      console.error('üí• UNEXPECTED ERROR in signup function:')
      console.error('- Error type:', typeof error)
      console.error('- Error message:', error.message)
      console.error('- Error stack:', error.stack)
      console.error('- Full error:', error)
      return { data: null, error }
    } finally {
      setLoading(false)
    }
  }

  const signIn = async (email, password) => {
    try {
      setLoading(true)
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password
      })

      if (error) {
        throw error
      }

      return { data, error: null }
    } catch (error) {
      console.error('Signin error:', error)
      return { data: null, error }
    } finally {
      setLoading(false)
    }
  }

  const signOut = async () => {
    try {
      setLoading(true)
      const { error } = await supabase.auth.signOut()
      
      if (error) {
        throw error
      }
      
      setUser(null)
      setProfile(null)
    } catch (error) {
      console.error('Signout error:', error)
    } finally {
      setLoading(false)
    }
  }

  const updateProfile = async (updates) => {
    try {
      const { data, error } = await supabase
        .from('user_profiles')
        .update(updates)
        .eq('id', user.id)
        .select()
        .single()

      if (error) {
        throw error
      }

      setProfile(data)
      return { data, error: null }
    } catch (error) {
      console.error('Profile update error:', error)
      return { data: null, error }
    }
  }

  const value = {
    user,
    profile,
    loading,
    signUp,
    signIn,
    signOut,
    updateProfile
  }

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  )
}